# round() fonksiyonu sayıları yuvarlıyor

print(round(4.52))              # (run: 5 )
print(round(1.25))              # (run: 1 )

# round fonksiyonunda virgülden sonraki yazacağın sayı, basamak sayısını belirleyebilir:
print(round(1.25,1))           # (run: 1.2)
print(round(1.25,2))           # (run: 1.25)

print(round(3.425698745,3))    # (run: 3.426)
