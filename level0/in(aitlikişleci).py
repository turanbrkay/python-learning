cümlemiz="abcd"

print("a" in cümlemiz)          # (run:True)  doğru dedi demekki cümlemiz içinde a harfi varmış

print("e" in cümlemiz)          # (run:False) yanlış dedi demekki cümlemiz içinde e harfi yok


# Gördüğünüz gibi, in adlı bu işleç, bir öğenin, veri tipi içinde bulunup bulunmadığını sorguluyor.
# Eğer bahsedilen öğe, veri tipi içinde geçiyorsa True çıktısı, eğer geçmiyorsa False çıktısı alıyoruz.